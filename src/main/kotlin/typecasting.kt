fun main(args: Array<String>) {
    println("56.456 convert to integer" +(56.456.toInt()))
    println("56.12F convert to long" +(56.12F.toLong()))
    println("56 convert to float" +(56.toFloat()))
    println("56 convert to double" +(56.toDouble()))
    //alternative
    var num =56.787F
    println(num.toInt())
    println(num.toLong())
}